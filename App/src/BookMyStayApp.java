/**
 * UseCase1HotelBookingApp
 *
 * This class represents the entry point of the Book My Stay
 * Hotel Booking Management System. It demonstrates how a
 * Java application starts execution using the main() method
 * and prints a welcome message to the console.
 *
 * @author Munna
 * @version 1.0
 */

public class UseCase1HotelBookingApp {

    /**
     * Main method - entry point of the Java application.
     * JVM starts execution from here.
     *
     * @param args command line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("=================================");
        System.out.println("   Welcome to Book My Stay App   ");
        System.out.println("   Hotel Booking System v1.0     ");
        System.out.println("=================================");

        System.out.println("Application started successfully.");

        // Application terminates after printing the message
    }
}
